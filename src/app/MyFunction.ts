export function addition(num1:any,num2:any){
    return num1+num2
}
export function sub(num1:any,num2:any){
    return num1-num2
}
export function mul(num1:any,num2:any){
    return num1*num2
}
export function div(num1:any,num2:any){
    return num1/num2
}
